package math

var MultiplyBase int = 5

// MultiplyFloat multiplies two float64 values
func MultiplyFloat(a float64, b float64) float64 {
	return a * b
}

// AddFloat adds two float64 values
func AddFloat(a float64, b float64) float64 {
	return a + b
}

// SubFloat subtracts two float64 values
func SubFloat(a float64, b float64) float64 {
	return a - b
}
